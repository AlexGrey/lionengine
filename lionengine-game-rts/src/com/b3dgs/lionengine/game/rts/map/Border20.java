package com.b3dgs.lionengine.game.rts.map;

/**
 * Represents the list of supported angles for area linking.
 * 
 * @see Border20Map
 */
public enum Border20
{
    /** Top. */
    TOP,
    /** Right. */
    RIGHT,
    /** Down. */
    DOWN,
    /** Left. */
    LEFT,
    /** Top left. */
    TOP_LEFT,
    /** Top right. */
    TOP_RIGHT,
    /** Down right. */
    DOWN_RIGHT,
    /** Down left. */
    DOWN_LEFT,
    /** Corner top left. */
    CORNER_TOP_LEFT,
    /** Corner top right. */
    CORNER_TOP_RIGHT,
    /** Corner down right. */
    CORNER_DOWN_RIGHT,
    /** Corner down left. */
    CORNER_DOWN_LEFT,
    /** Corner out top down. */
    CORNER_OUT_TOP_DOWN,
    /** Corner out down top. */
    CORNER_OUT_DOWN_TOP,
    /** Corner in down top. */
    CORNER_IN_DOWN_TOP,
    /** Corner in top down. */
    CORNER_IN_TOP_DOWN,
    /** Center. */
    CENTER,
    /** Top middle. */
    TOP_MIDDLE,
    /** Down middle. */
    DOWN_MIDDLE,
    /** Right middle. */
    RIGHT_MIDDLE,
    /** Left middle. */
    LEFT_MIDDLE,
    /** Unknown. */
    UNKNOWN,
    /** None. */
    NONE;
}
