package com.b3dgs.lionengine.swing;

/**
 * Action combo abstract class.
 */
public abstract class ActionCombo
{
    /**
     * Constructor.
     */
    public ActionCombo()
    {
        // Nothing to do
    }

    /**
     * Action function.
     * 
     * @param item The item reference.
     */
    public abstract void action(Object item);
}
